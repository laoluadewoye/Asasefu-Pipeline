package com.laoluade.ingestor.ao3.repositories;

/**
 * <p>This enum defines the parse types the archive ingestor application supports.</p>
 */
public enum ArchiveParseType {
    /**
     * This enum value is for indicating that a chapter is being parsed.
     */
    CHAPTER,

    /**
     * This enum value is for indicating that a story is being parsed.
     */
    STORY
}
